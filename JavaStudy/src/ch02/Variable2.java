package ch02;

public class Variable2 {

	public static void main(String[] args) {
		int num = 65;
		System.out.println((char)num); // 문자로 형변환 진행
		int num2 = num +26;
		System.out.println((char)+num2);
		
		char ch = 'a';
		char ch2 = 'z';
		System.out.println((int)ch);
		System.out.println((int)ch2);
		
	}

}
